#!/bin/sh

OS_NAME=$(uname -s)

# Aliases ======================================================================

# ---------------------------------------------------------------------------- #
# General                                                                      #
# ---------------------------------------------------------------------------- #

# For shortcuts.
alias refresh='browser-sync start --server --files "*/*"'

# ---------------------------------------------------------------------------- #
# Mac OS X - Darwin                                                            #
# ---------------------------------------------------------------------------- #

if [ $OS_NAME == "Darwin" ] ; then
  alias atom='open -a Atom.app'
  alias subl='open -a Sublime\ Text.app'
  alias chrome='open -a Chrome.app'
  alias firefox='open -a Firefox.app'
  alias brew="$HOME/goinfre/brew/bin/./brew"
fi

# ---------------------------------------------------------------------------- #
# Linux                                                                        #
# ---------------------------------------------------------------------------- #

if [ $OS_NAME == "Linux" ] ; then
  alias open="xdg-open"
fi

# ====================================================================== Aliases
